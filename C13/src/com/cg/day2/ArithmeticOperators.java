public class ArithmeticOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		System.out.println("add = "+(a+b));
		System.out.println("sub = "+(a-b));
		System.out.println("multiply = "+(a*b));
		System.out.println("division  = "+(a/b));
		
	}

}
